CREATE TABLE car(
ID int NOT NULL AUTO_INCREMENT,
name VARCHAR(100),
miles DOUBLE,
cylinders DOUBLE,
displacement DOUBLE,
horsepower DOUBLE,
weight DOUBLE,
acceleration DOUBLE,
year DOUBLE,
origin VARCHAR(100),
logo VARCHAR(100),
PRIMARY KEY (ID)
);


